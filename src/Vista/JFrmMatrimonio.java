    private void btn_agregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_agregarActionPerformed
        // TODO add your handling code here:
        try {
            String descripcion = txt_descripcion.getText();
            int cantidad = Integer.parseInt(spn_cant.getValue().toString());
            int cantidad_recinto = Integer.parseInt(spn_cantidad_mesas.getValue().toString());
            int tamano_recinto = Integer.parseInt(txt_tamano.getText());
            int precio = Integer.parseInt(txt_precio.getText());
            int cantidad_platos = Integer.parseInt(spn_cantidad_platos.getValue().toString());
            boolean musica = (rdb_musica.isSelected() ? true:false);
            Matrimonio ma = new Matrimonio(cantidad_platos, musica, descripcion, cantidad, tamano_recinto, cantidad_platos, precio);
            boolean resp = dao.guardarEvento(ma);
            if (resp) {
                JOptionPane.showMessageDialog(null, "Grabo");
            } else {
                JOptionPane.showMessageDialog(null, "No Grabo");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_btn_agregarActionPerformed

